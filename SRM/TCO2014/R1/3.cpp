class EllysLamps {
public:
	int getMin(string);
};

int EllysLamps::getMin(string L) {
	int n = L.size();
	int ret=0;
	vector<bool> used;
	for(int i=0;i<n;++i)used.push_back(0);
	for(int i=0;i<n;++i)
    {
        if(L[i]=='Y'&&!used[i])
        {
            if(i>0&&!used[i-1]&&L[i-1]=='N')
            {
                used[i-1]=1;
                used[i]=1;
                ++ret;
            }
            else if(i+1<n&&!used[i+1]&&L[i+1]=='N')
            {
                used[i+1]=1;
                used[i]=1;
                ++ret;
            }
            else if(i-1>=0&&i+1<n&&!used[i-1]&&!used[i+1]&&L[i-1]=='Y'&&L[i+1]=='Y')
            {
                used[i+1]=1;
                used[i]=1;
                used[i-1]=1;
                ++ret;
            }
        }
    }
    return ret;
}

