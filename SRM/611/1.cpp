class InterestingNumber {
public:
	string isInteresting(string);
};

string InterestingNumber::isInteresting(string x) {
    int n = x.size();
    int dig[10];
    for(int i=0;i<10;++i)dig[i]=0;
	for(int i=0;i<n;++i)
    {
        ++dig[ x[i]-'0' ];
    }
    bool ok=true;
    for(int i=0;i<10;++i)
    {
        if(dig[i]!=0&&dig[i]!=2)
        {
            ok=false;
        }
    }
    if(!ok)
    {
        return "Not interesting";
    }
    else
    {
        bool vis[55];
        for(int i=0;i<n;++i)vis[i]=false;
        for(int i=0;i<n;++i)
        {
            int c = x[i];
            if(vis[i])continue;
            for(int j=i+1;j<n;++j)
            {
                if(c==x[j]&&!vis[j])
                {
                    if(j-i-1!=(c-'0'))
                    {
                        ok=false;
                    }
                    vis[i]=1;
                    vis[j]=1;
                    break;
                }
            }
        }
        return ok?"Interesting":"Not interesting";
    }

}

