class MicroStrings {
public:
	string makeMicroString(int A, int D) {
        vector<int> a;
        while(A>=0)
        {
            a.push_back(A);
            A-=D;
        }
        string ret="";
        for(int i=0;i<a.size();++i)
        {
            ret += dtos(a[i]);
        }
        return ret;
    }
    string dtos(int x)
    {
        string ret="";
        if(x==0)
        {
            ret="0";
        }
        else
        {
            while(x>0)
            {
                ret+= (x%10+'0');
                x/=10;
            }
            reverse(ret.begin(),ret.end());
        }
        return ret;
    }
};
